package com.kafka.event.status;

/**
 * The enum Status.
 */
public enum Status {
    /**
     * Pending status.
     */
    PENDING,
    /**
     * Not processed status.
     */
    NOT_PROCESSED,
    /**
     * Success status.
     */
    SUCCESS,
    /**
     * Failed status.
     */
    FAILED,
    /**
     * Partial success status.
     */
    PARTIAL_SUCCESS,
    /**
     * In progress status.
     */
    IN_PROGRESS,
    /**
     * Skipped status.
     */
    SKIPPED
}


